public class Tarea_1_5 {
    public static void main(String[] args) throws Exception {

        for (int i = 8; i >= 0; i--) {

            for (int j = 0; j < 9; j++) {

                if (i > j) {

                    System.out.print(" ");

                }

                else {

                    System.out.print(" *");

                }

            }

            System.out.println();

        }
    }
}
