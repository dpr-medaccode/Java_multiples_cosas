    private void jButtonAnnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnnadirActionPerformed

        String idstr = JOptionPane.showInputDialog("Inserta el ID");

        if (idstr == null) {

            //JOptionPane.showMessageDialog(null, "Operación cancelada.");
            JOptionPane.showMessageDialog(null, "Operación cancelada.", "", JOptionPane.WARNING_MESSAGE);

            return; // interesante
        }
        
        int id;

        try {

            id = Integer.parseInt(idstr);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Inserta un numero valido", "", JOptionPane.WARNING_MESSAGE);
            return;

        }

        String nombre = JOptionPane.showInputDialog("Inserta el Nombre");

        if (nombre == null) {

            //JOptionPane.showMessageDialog(null, "Operación cancelada.");
            JOptionPane.showMessageDialog(null, "Operación cancelada.", "", JOptionPane.WARNING_MESSAGE);

            return;
        }

        String preciostr = JOptionPane.showInputDialog("Inserta el precio");

        if (preciostr == null) {

            //JOptionPane.showMessageDialog(null, "Operación cancelada.");
            JOptionPane.showMessageDialog(null, "Operación cancelada.", "", JOptionPane.WARNING_MESSAGE);

            return;
        }
        
        float precio;
        
         try {

            precio = Float.parseFloat(preciostr);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Inserta un numero valido", "", JOptionPane.WARNING_MESSAGE);
            return;

        }
         
         Producto producto = new Producto (id, nombre, precio);
         
         boolean annadido = lista.add(producto);
         
         if (!annadido){
             
             JOptionPane.showMessageDialog(null, "El producto esta repetido", "", JOptionPane.WARNING_MESSAGE);
           
         }
         
         recargar();

        //JOptionPane.showMessageDialog(null, id);

    }//GEN-LAST:event_jButtonAnnadirActionPerformed

    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
      
        String idstr = JOptionPane.showInputDialog("Inserta el ID");

        if (idstr == null) {

            //JOptionPane.showMessageDialog(null, "Operación cancelada.");
            JOptionPane.showMessageDialog(null, "Operación cancelada.", "", JOptionPane.WARNING_MESSAGE);

            return; // interesante
        }
        
        int id;

        try {

            id = Integer.parseInt(idstr);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Inserta un numero valido", "", JOptionPane.WARNING_MESSAGE);
            return;

        }
        
        Producto eliminable = null;
        
        for(Producto producto : lista){
            
            if(producto.getId() == id) eliminable = producto;
        
        }
        
        if(eliminable  == null) {
            JOptionPane.showMessageDialog(null, "No existe un producto con ese id", "", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        lista.remove(eliminable);
        
        recargar();
            
        
    }//GEN-LAST:event_jButtonEliminarActionPerformed

