    private void recargar(){
        
        listId.removeAll();
        listNombre.removeAll();
        listPrecio.removeAll();
        
        for (Producto producto : lista){
        
            listId.add(String.valueOf(producto.getId()));
            listNombre.add(producto.getNombre());
            listPrecio.add(String.valueOf(producto.getPrecio()));
        
        }
        
    }
    
