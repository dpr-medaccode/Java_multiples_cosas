public class Formas6 {
    public static void main(String[] args) throws Exception {

        for (int i = 4; i >= 0; i--) {

            for (int j = 0; j < 5; j++) {

                if (i > j) {

                    System.out.print("  ");

                }

                else {

                    System.out.print("* ");
                    
                }

            }

            System.out.println();

        }

    }
}
