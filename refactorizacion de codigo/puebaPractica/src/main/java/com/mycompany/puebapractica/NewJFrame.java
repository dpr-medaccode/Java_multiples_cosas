    private Connection conectar() {
        try {
            // Ajusta la URL de conexión para que apunte al contenedor MySQL
            String url = "jdbc:mysql://localhost:3306/testdb"; // Usamos testdb como la base de datos
            String usuario = "user"; // El usuario configurado en Docker
            String contraseña = "pass"; // La contraseña configurada en Docker

            // Establece la conexión
            return DriverManager.getConnection(url, usuario, contraseña);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error de conexión: " + e.getMessage());
            return null;
        }

    }

