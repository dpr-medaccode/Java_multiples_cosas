    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nombre = jTextField1.getText();
        String pista = (String) jComboBox1.getSelectedItem();
        String fecha = jTextField2.getText();  // Asegúrate de que la fecha esté en formato correcto (YYYY-MM-DD)
        int duracion = Integer.parseInt((String) jComboBox2.getSelectedItem());
        String hora = (String) jComboBox3.getSelectedItem();  // Asegúrate de que 'hora' esté en formato HH:mm:ss

// Imprimir para debuguear
        System.out.println(fecha);

// Conectar con la base de datos
        Connection con = conectar();

        if (con != null) {
            try {
                // Preparar la consulta SQL para insertar los datos
                String sql = "INSERT INTO reservas (nombre, pista, fecha, hora_inicio, duracion) VALUES (?, ?, ?, ?, ?)";
                PreparedStatement ps = con.prepareStatement(sql);

                // Establecer los valores de la consulta
                ps.setString(1, nombre);              // 'nombre' es de tipo VARCHAR
                ps.setString(2, pista);               // 'pista' es de tipo VARCHAR
                ps.setString(3, fecha);               // 'fecha' es de tipo DATE (asegúrate de que esté en formato correcto)
                ps.setTime(4, Time.valueOf(hora));    // 'hora_inicio' es de tipo TIME (asegúrate de que 'hora' esté en formato HH:mm:ss)
                ps.setInt(5, duracion);               // 'duracion' es de tipo INT

                // Ejecutar la consulta
                int filasAfectadas = ps.executeUpdate();
                if (filasAfectadas > 0) {
                    JOptionPane.showMessageDialog(this, "Reserva guardada con éxito.");
                } else {
                    JOptionPane.showMessageDialog(this, "Error al guardar la reserva.");
                }

                // Cerrar la conexión
                ps.close();
                con.close();

            } catch (Exception e) {
                e.printStackTrace();  // Esto mostrará el error completo de la base de datos
                JOptionPane.showMessageDialog(this, "Error al guardar la reserva: " + e.getMessage());
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

