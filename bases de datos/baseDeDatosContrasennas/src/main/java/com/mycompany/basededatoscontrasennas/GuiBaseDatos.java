    private void initBaseDatos() {

        try (Connection conn = DriverManager.getConnection(jdbcUrl, user, password)) {
            stmt = conn.createStatement();

            recagraBaseDatos();

        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            try {
                stmt.close();
            } catch (SQLException e) {
                
                e.printStackTrace();
            }

        }

    }

    private void recagraBaseDatos() {

        jTable1.clearSelection();

        try {
            ResultSet rs = stmt.executeQuery("SELECT * FROM users");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            model.setRowCount(0);

            while (rs.next()) {
                Usuario usuario = new Usuario(rs.getString("servicio"), rs.getString("nombre"), rs.getString("contrasenna"));
                
                if(!ocultar){
                
                model.addRow(new Object[]{usuario.getServicio(), usuario.getNombre(), usuario.getContrasenna()});
                
                }else{
                    
                    model.addRow(new Object[]{usuario.getServicio(), usuario.getNombre(), "******"});
                
                }

                

            }

        } catch (SQLException e) {
            e.printStackTrace();
        }

    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        char currentEchoChar = jPasswordField1.getEchoChar();

        // Si est치 mostrando asteriscos, cambiar a texto normal (0)
        if (currentEchoChar == '*') {
            jPasswordField1.setEchoChar((char) 0);  // Mostrar texto normal

        } else {
            jPasswordField1.setEchoChar('*');  // Volver a mostrar asteriscos

        }
        ocultar = !ocultar;
        
        initBaseDatos();
        recagraBaseDatos();

    }//GEN-LAST:event_jButton2ActionPerformed

