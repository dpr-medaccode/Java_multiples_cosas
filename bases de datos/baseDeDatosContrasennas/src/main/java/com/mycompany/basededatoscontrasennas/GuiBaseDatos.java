    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Usuario annadir = new Usuario(jTextField1.getText(), jTextField2.getText(), jPasswordField1.getText());

        String sql = "INSERT INTO users (servicio, nombre, contrasenna) VALUES (?, ?, ?)";
        try (Connection conn = DriverManager.getConnection(jdbcUrl, user, password)) {
            // Crear un PreparedStatement
            PreparedStatement pst = conn.prepareStatement(sql);

            // Establecer los valores de los par치metros en la consulta
            pst.setString(1, annadir.getServicio());
            pst.setString(2, annadir.getNombre());
            pst.setString(3, annadir.getContrasenna());

            // Ejecutar la consulta de inserci칩n
            int filasAfectadas = pst.executeUpdate();

            // Mostrar un mensaje si la inserci칩n fue exitosa
            if (filasAfectadas > 0) {
                System.out.println("Usuario insertado correctamente.");
            } else {
                System.out.println("Error al insertar el usuario.");
            }
            
            initBaseDatos();
            recagraBaseDatos();
            
        } catch (SQLException e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

