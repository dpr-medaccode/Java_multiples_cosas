    private void insertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertarActionPerformed

        String nombre = textNombre.getText();
        String pista = (String) boxPista.getSelectedItem();
        LocalDate fecha = LocalDate.parse(TextFecha.getText()); // Extrae la fecha de TextFecha
        LocalTime hora = LocalTime.parse(boxHora.getSelectedItem().toString()); // Extrae la hora de boxHora
        int duracion = Integer.parseInt(boxDuracion.getSelectedItem().toString()); // Duración (60 o 120)

        LocalDateTime fechaHora = LocalDateTime.of(fecha, hora);

        Reserva reserva = new Reserva(nombre, pista, fechaHora, duracion);

        String sql = "INSERT INTO reservas (nombre, fecha, pista, hora, duracion) VALUES (?, ?, ?, ?, ?)";

        try {

            PreparedStatement stmt = conn.prepareStatement(sql);

            stmt.setString(1, reserva.getNombre());

            stmt.setDate(2, java.sql.Date.valueOf(reserva.getFecha().toLocalDate()));

            stmt.setInt(3, reserva.getPista());
   
            stmt.setTime(4, java.sql.Time.valueOf(reserva.getFecha().toLocalTime()));
    
            stmt.setInt(5, reserva.getDuracion());
            
            stmt.executeUpdate();
            
            recargarDia();

        } catch (Exception e) {

            e.printStackTrace();

        }


    }//GEN-LAST:event_insertarActionPerformed

