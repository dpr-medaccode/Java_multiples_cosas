    public int conversorHora(String hora) {
        // Partimos la hora usando ":"
        String[] partes = hora.split(":");

        // Obtenemos la hora como número
        int horas = Integer.parseInt(partes[0]);

        // Suponiendo que tu tabla empieza en 9:00
        int fila = horas - 9;

        return fila;
    }

